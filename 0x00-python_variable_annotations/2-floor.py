#!/usr/bin/env python3

"""
This script defines a function to calculate the floor of a float.
"""
import math


def floor(n: float) -> int:
    """
    Calculate and return the floor of a float

    Args:
        n (float): The input floating-point number

    Returns:
        int: The floor of the input number as an integer
    """
    return math.floor(n)
